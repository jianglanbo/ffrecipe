// Main function
function main() {
  const args = process.argv.slice(2);
  const categoryIndex = args.indexOf('--category');
  const topicIndex = args.indexOf('--topic');
  const batchIndex = args.indexOf('--batch');
  const allIndex = args.indexOf('--all');

  // Generate all topics
  if (allIndex !== -1) {
    console.log('Generating sample articles for all categories...\n');
    
    Object.entries(TOPIC_TEMPLATES).forEach(([category, topics]) => {
      const categoryDir = path.join(CONTENT_DIR, category);
      
      if (!fs.existsSync(categoryDir)) {
        fs.mkdirSync(categoryDir, { recursive: true });
      }
      
      topics.forEach(topic => {
        const article = generateSampleArticle(topic, category);
        const filePath = path.join(categoryDir, `${slugify(topic.title)}.mdx`);
        
        fs.writeFileSync(filePath, article);
        console.log(`Created: ${filePath}`);
      });
    });
    
    console.log('\n✅ All sample articles generated!');
    return;
  }

  // Generate single article
  if (categoryIndex !== -1 && topicIndex !== -1) {
    const category = args[categoryIndex + 1];
    const topicTitle = args[topicIndex + 1];
    
    const article = generateSampleArticle({ title: topicTitle, keywords: [category] }, category);
    const categoryDir = path.join(CONTENT_DIR, category);
    
    if (!fs.existsSync(categoryDir)) {
      fs.mkdirSync(categoryDir, { recursive: true });
    }
    
    const filePath = path.join(categoryDir, `${slugify(topicTitle)}.mdx`);
    fs.writeFileSync(filePath, article);
    
    console.log(`✅ Created: ${filePath}`);
    return;
  }

  // Show usage
  console.log(`
Usage:
  node scripts/generate-content.js --all                    Generate all sample articles
  node scripts/generate-content.js --category <cat> --topic "<title>"   Generate single article

Examples:
  node scripts/generate-content.js --all
  node scripts/generate-content.js --category life-insurance --topic "How to Buy Life Insurance"
`);
}

main();
