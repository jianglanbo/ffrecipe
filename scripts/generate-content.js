// Article template
function generateArticleFrontMatter(options) {
  const { title, slug, description, category, author, tags, featured } = options;
  const now = new Date().toISOString();
  
  return `---
title: "${title}"
slug: "${slug}"
description: "${description}"
category: "${category}"
author: "${author || 'FFRecipe Team'}"
date: "${now}"
lastModified: "${now}"
tags: ${JSON.stringify(tags || [])}
featured: ${featured || false}
---

`;
}

